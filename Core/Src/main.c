void LED_Control(void)
{
	TIM2_Count++;
	if(TIM2_Count>=Sample_Frequency*LED_Frequency)
	{
		TIM2_Count=0;
		LED_Frequency=LED_Frequency+0.2f;
		if(LED_Frequency>=2.0f)LED_Frequency=1.0f;
	}
	TIM_Duty=(float)TIM2_Count/Sample_Frequency*LED_Frequency;
	if(TIM_Duty>=1)TIM_Duty=TIM_Duty-1;
	cordic_calculate_start(TIM_Duty);//输入为0-1
	cordic_get_result(&sin_test,&cos_test);
	__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_1,1000*sin_test+1000);//呼吸灯的占空比改变
}

